  React.ComponentRef<typeof TabsPrimitive.Trigger>,
  React.ComponentPropsWithoutRef<typeof TabsPrimitive.Trigger>
>(({ className, ...props }, ref) => (
  <TabsPrimitive.Trigger
    ref={ref}
    className={cn(
      "inline-flex items-center gap-1.5 rounded px-3 py-1.5 font-medium uppercase tracking-wider text-muted transition-colors hover:text-ink focus-visible:outline-none data-[state=active]:bg-scan/15 data-[state=active]:text-scan",
      className,
    )}
    {...props}
  />
));
TabsTrigger.displayName = TabsPrimitive.Trigger.displayName;

